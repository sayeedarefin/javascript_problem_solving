// Create an array with numerical values and use a for loop to calculate and print the average of the elements.

var numbers = [2,4,6,8,10]
sum = 0;


for(i=0; i<numbers.length; i++){
    sum += numbers[i];
}
var avg = sum/numbers.length;
console.log(avg)





